{
	struct qmp_combo *qmp = phy_get_drvdata(phy);
	int ret;

	mutex_lock(&qmp->phy_mutex);
	ret = qmp_combo_usb_power_off(phy);
	if (ret)
		goto out_unlock;

	ret = qmp_combo_com_exit(qmp, false);
	if (ret)
		goto out_unlock;

	qmp->usb_init_count--;

out_unlock:
	mutex_unlock(&qmp->phy_mutex);
	return ret;
}

